INSERT INTO kalendarz (data_k, dzien_t,dzien_n, miesiac, rok, wolne)
SELECT cal.date as data_k, WEEKDAY(cal.date) as dzien_t, 
case 
  when WEEKDAY(cal.date)= 0 then 'Poniedziałek'
  when WEEKDAY(cal.date) = 1 then 'Wtorek'
  when WEEKDAY(cal.date) = 2 then 'Środa'
  when WEEKDAY(cal.date) = 3 then 'Czwartek'
  when WEEKDAY(cal.date) = 4 then 'Piątek'
  when WEEKDAY(cal.date) = 5 then 'Sobota'
  when WEEKDAY(cal.date) = 6 then 'Niedziela'
