end as dzien_n 
,MONTH(cal.date) as miesiac, YEAR(cal.date) as rok , 
if(WEEKDAY(cal.date) > 4,1,0) as wolne
FROM (
SELECT d_od + INTERVAL c.i* 100 + b.i * 10 + a.i DAY as date 
FROM licznik a JOIN licznik b JOIN licznik c  						--  kartezjan 
ORDER BY  c.i*100 + b.i*10 + a.i) cal
WHERE cal.date BETWEEN d_od AND d_do; 

